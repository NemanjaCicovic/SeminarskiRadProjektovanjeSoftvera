    private void prepareView() {
        fillCbPlace();
    }

    private void fillCbPlace() {
        cmbPlace.removeAllItems();
        List<Place> places = Controller.getInstance().getAllPlaces();
        for(Place place : places){
            cmbPlace.addItem(place);
        }
    }
    
    private void resetForm() {
        txtWorkerID.setText("");
        txtFirstname.setText("");
        txtLastname.setText("");
    }
    
    private void validateWorker()throws Exception{
        String errorMessage = "";
        if(txtWorkerID.getText().isEmpty() && txtFirstname.getText().isEmpty() && txtLastname.getText().isEmpty()){
            lblWorkerIdError.setText("Worker ID can not be empty!");
            lblFirstnameError.setText("Firstname can not be empty!");
            lblLastnameError.setText("Lastname can not be empty!");
            errorMessage += "Worker ID can not be empty!\nFirstname can not be empty!\nLastname can not be empty!\n";
            throw new TotalWorkerException(errorMessage);
        }
        if(txtWorkerID.getText().isEmpty() && txtFirstname.getText().isEmpty()){
            lblLastnameError.setText("");
            lblWorkerIdError.setText("Worker ID can not be empty!");
            lblFirstnameError.setText("Firstname can not be empty!");
            errorMessage += "Worker ID can not be empty!\nFirstname can not be empty!";
            throw new TotalWorkerException(errorMessage);
        }
        if(txtWorkerID.getText().isEmpty() && txtLastname.getText().isEmpty()){
            lblFirstnameError.setText("");
            lblWorkerIdError.setText("Worker ID can not be empty!");
            lblLastnameError.setText("Lastname can not be empty!");
            errorMessage += "Worker ID can not be empty!\nLastname can not be empty!\n";
            throw new TotalWorkerException(errorMessage);
        }
        if(txtFirstname.getText().isEmpty() && txtLastname.getText().isEmpty()){
            lblWorkerIdError.setText("");
            lblFirstnameError.setText("Firstname can not be empty!");
            lblLastnameError.setText("Lastname can not be empty!");
            errorMessage += "Firstname can not be empty!\nLastname can not be empty!\n";
            throw new TotalWorkerException(errorMessage);
        }
        if(txtWorkerID.getText().isEmpty()){
            lblWorkerIdError.setText("Worker ID can not be empty!");
            lblFirstnameError.setText("");
            lblLastnameError.setText("");
            errorMessage += "Worker ID can not be empty!\n";
            throw new WorkerIdException(errorMessage);
        }else{
            lblWorkerIdError.setText("");
        }
        if(txtFirstname.getText().isEmpty()){
            lblWorkerIdError.setText("");
            lblFirstnameError.setText("Firstname can not be empty!");
            lblLastnameError.setText("");
            errorMessage += "Firstname can not be empty!\n";
            throw new FirstnameWorkerException(errorMessage);
        }else{
            lblFirstnameError.setText("");
        }
        if(txtLastname.getText().isEmpty()){
            lblWorkerIdError.setText("");
            lblFirstnameError.setText("");
            lblLastnameError.setText("Lastname can not be empty!");
            errorMessage += "Lastname can not be empty!\n";
            throw new LastnameWorkerException(errorMessage);
        }else{
            lblLastnameError.setText("");
        }
        if(!errorMessage.isEmpty()){
            throw new Exception(errorMessage);
        }
    }
