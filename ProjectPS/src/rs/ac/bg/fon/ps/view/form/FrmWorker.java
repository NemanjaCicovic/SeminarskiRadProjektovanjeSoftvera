    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Worker worker = new Worker();
            validateWorker();
            worker.setWorkerId(Long.parseLong(txtWorkerID.getText().trim()));
            validateWorker();
            worker.setFirstName(txtFirstname.getText().trim());
            validateWorker();validateWorker();
            worker.setLastName(txtLastname.getText().trim());
            validateWorker();
            worker.setPlace((Place) cmbPlace.getSelectedItem());
            Controller.getInstance().addWorker(worker);
            JOptionPane.showMessageDialog(this, "Worker successfully saved!", "Insert worker", JOptionPane.INFORMATION_MESSAGE);
            resetForm();
        } catch (WorkerIdException wie) {
            wie.printStackTrace();
            JOptionPane.showMessageDialog(this, wie.getInncorrect_workerId(), "Insert worker", JOptionPane.ERROR_MESSAGE);
        } catch (FirstnameWorkerException fwe) {
            fwe.printStackTrace();
            JOptionPane.showMessageDialog(this, fwe.getInncorrect_firstnameWorker(), "Insert worker", JOptionPane.ERROR_MESSAGE);
        } catch (LastnameWorkerException lwe) {
            lwe.printStackTrace();
            JOptionPane.showMessageDialog(this, lwe.getInncorrect_lastnameWorker(), "Insert worker", JOptionPane.ERROR_MESSAGE);
        } catch (TotalWorkerException twe) {
            twe.printStackTrace();
            JOptionPane.showMessageDialog(this, twe.getInncorrect_totalWorker(), "Insert worker", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Worker not saved!", "Insert worker", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

